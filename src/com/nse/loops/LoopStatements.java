package com.nse.loops;

public class LoopStatements {

	public static void main(String[] args) {

		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}

		int[] numbers = { 45, 95, 89, 63, 65, 15, 23 };

		for (int i = 0; i < numbers.length; i++) {
			System.out.println(numbers[i]);
		}
	}

}
