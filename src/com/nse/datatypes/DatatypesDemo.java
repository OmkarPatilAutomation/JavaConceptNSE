package com.nse.datatypes;

public class DatatypesDemo {

	public static void main(String[] args) {
		
		byte a = 127;
		int b = 1235;
		short c = 12;
		
		
		
		long d = 8778789878L;
		
		double f = 10.0;
		
		float e = 10.0f;
		
		char g = 'h';
		
		boolean check = true;
		
		System.out.println(a);
		
		
		int radius = 10;
		double pi = 3.14;
		
		double areaOfCircle = radius * radius * pi;
		
		System.out.println(areaOfCircle);
		
		
		//Type Casting
	}
}
